class CookieCutter:
    def __init__(self, chocolate_chips):
        self.chocolate_chips = chocolate_chips

Cookie1 = CookieCutter(5)
print("Cookie 1 has " + str(Cookie1.chocolate_chips) + " chocolate chips")

Cookie2 = CookieCutter(12)
print("Cookie 2 has " + str(Cookie2.chocolate_chips) + " chocolate chips")